
# Uso default
print("Brasil ganhou 5 títulos mundiais")

print("Brasil", "ganhou",  5, "títulos", "mundiais")

# Uso do 'sep'. Valor default é o espaço " "
print("Brasil", "ganhou", 5, "títulos", "mundiais", sep=", ")

# Uso do 'end'. Valor default é a quebra de linha "\n"
print("Brasil", "ganhou", 5, "títulos", "mundiais", sep=" ", end="\n\n*** Final do Print ***\n\n")


